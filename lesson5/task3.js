////// for
let number = 7;
for (let i = 1; i < 11; i++) {
  let result = number * i;
  console.log(`${number} X ${i} = ${result}`);
}

let number1 = 7;
let i = 1;
for (; i < 11; i++) {
  let result1 = number1 * i;
  console.log(`${number1} X ${i}= ${result1}`);
}

//////while
let number2 = 7;
let count = 1;
while (count < 11) {
  let result3 = number2 * count;
  console.log(`${number2} X ${count} = ${result3}`);
  count++;
}
